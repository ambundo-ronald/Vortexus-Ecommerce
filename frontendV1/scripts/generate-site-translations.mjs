import fs from "node:fs";
import path from "node:path";
import { createHash } from "node:crypto";
import { parse } from "@babel/parser";
import traverseModule from "@babel/traverse";

const traverse = traverseModule.default;
const sourceRoot = path.resolve("src");
const outputFile = path.join(sourceRoot, "locales", "siteTranslations.generated.js");
const visibleAttributes = new Set(["placeholder", "aria-label", "title"]);
const phrases = new Set();

function normalize(value) {
  return value.replace(/\s+/g, " ").trim();
}

function collectFiles(directory) {
  return fs.readdirSync(directory, { withFileTypes: true }).flatMap((entry) => {
    const entryPath = path.join(directory, entry.name);
    if (entry.isDirectory()) return collectFiles(entryPath);
    return entryPath.endsWith(".jsx") ? [entryPath] : [];
  });
}

for (const file of collectFiles(sourceRoot)) {
  const ast = parse(fs.readFileSync(file, "utf8"), { sourceType: "module", plugins: ["jsx"] });
  traverse(ast, {
    JSXText({ node }) {
      const phrase = normalize(node.value);
      if (/[A-Za-z]{2}/.test(phrase)) phrases.add(phrase);
    },
    JSXAttribute({ node }) {
      if (!visibleAttributes.has(node.name?.name) || node.value?.type !== "StringLiteral") return;
      const phrase = normalize(node.value.value);
      if (/[A-Za-z]{2}/.test(phrase)) phrases.add(phrase);
    },
  });
}

const orderedPhrases = [...phrases].sort((a, b) => a.localeCompare(b));

async function translatePhrase(phrase, target) {
  const query = new URLSearchParams({ client: "gtx", sl: "en", tl: target, dt: "t", q: phrase });
  const response = await fetch(`https://translate.googleapis.com/translate_a/single?${query}`);
  if (!response.ok) throw new Error(`Translation request failed (${response.status}) for ${target}`);
  const payload = await response.json();
  return payload[0].map((part) => part[0]).join("");
}

async function translateLanguage(target) {
  const translated = {};
  let cursor = 0;
  const workers = Array.from({ length: 8 }, async () => {
    while (cursor < orderedPhrases.length) {
      const index = cursor++;
      const phrase = orderedPhrases[index];
      translated[phrase] = await translatePhrase(phrase, target);
    }
  });
  await Promise.all(workers);
  return translated;
}

const translations = { en: Object.fromEntries(orderedPhrases.map((phrase) => [phrase, phrase])) };
for (const language of ["sw", "fr", "ar", "hi", "zh-CN"]) {
  console.log(`Translating ${orderedPhrases.length} phrases to ${language}...`);
  translations[language] = await translateLanguage(language);
}

translations.zh = translations["zh-CN"];
delete translations["zh-CN"];

const keys = Object.fromEntries(orderedPhrases.map((phrase) => [phrase, `site_${createHash("sha1").update(phrase).digest("hex").slice(0, 12)}`]));
const resources = Object.fromEntries(Object.entries(translations).map(([language, values]) => [
  language,
  Object.fromEntries(Object.entries(values).map(([phrase, value]) => [keys[phrase], value])),
]));

fs.mkdirSync(path.dirname(outputFile), { recursive: true });
fs.writeFileSync(
  outputFile,
  `// Generated by scripts/generate-site-translations.mjs.\nexport const siteTranslationKeys = ${JSON.stringify(keys, null, 2)};\n\nexport const siteResources = ${JSON.stringify(resources, null, 2)};\n`,
);
console.log(`Wrote ${outputFile}`);
